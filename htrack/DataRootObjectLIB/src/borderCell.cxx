#include "../include/borderCell.h"


/****************************************************************
 * CLASS borderCell								 				*
 ****************************************************************/

/****************************************************************
 * Default constructor											*
 ****************************************************************/

borderCell::borderCell() {

	layer.clear();

}

/****************************************************************
 * Constructor													*
 ****************************************************************/

borderCell::borderCell(const borderCell& value) {

	this->layer = value.layer;

}

/****************************************************************
 * Destructor													*
 ****************************************************************/

borderCell::~borderCell() {

	layer.clear();

}

/****************************************************************
 * operator = ()												*
 ****************************************************************/

const borderCell& borderCell::operator = (const borderCell& value) {

	this->layer = value.layer;

	return *this;

}
